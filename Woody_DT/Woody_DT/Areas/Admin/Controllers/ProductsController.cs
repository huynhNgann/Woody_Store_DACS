using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Model.EF;
using Model.DAO;
namespace Woody_DT.Areas.Admin.Controllers
{
    public class ProductsController : BaseController
    {
        private ShopOnlineDbContext db = new ShopOnlineDbContext();
        public ActionResult Index(string searchString, int page = 1, int pageSize = 20)
        {
            var dao = new ProductDAO();
            var model = dao.ListAllPage(searchString, page, pageSize);

            ViewBag.SearchString = searchString;
            return View(model);
           // return View(db.Products.ToList());
        }
